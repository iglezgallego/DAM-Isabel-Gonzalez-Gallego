//Operadores aritméticos

double operando1 = 4;
double operando2 = 3;
double resultado;

//Suma
resultado = operando1 + operando2;
Console.WriteLine("La suma da como resultado: "+resultado);
//Resta
resultado = operando1 - operando2;
Console.WriteLine("La resta da como resultado: " + resultado);
//Multiplicación
resultado = operando1 * operando2;
Console.WriteLine("La multiplicación da como resultado: " + resultado);
//División
resultado = operando1 / operando2;
Console.WriteLine("La división da como resultado: " + resultado);
//Resto entero de la divisón
resultado = operando1 % operando2;
Console.WriteLine("El resto entero de la división da como resultado: " + resultado);



